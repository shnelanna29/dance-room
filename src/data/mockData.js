export const getScheduleForWeek = (weekOffset = 0) => {
  const startDate = new Date();
  startDate.setDate(startDate.getDate() + weekOffset * 7);
  const monday = new Date(startDate);
  monday.setDate(monday.getDate() - monday.getDay() + 1);

  const schedule = [];
  const days = ['Понедельник', 'Вторник', 'Среда', 'Четверг', 'Пятница', 'Суббота', 'Воскресенье'];
  const styles = danceStyles;
  const teachersNames = teachers;

  for (let i = 0; i < 7; i++) {
    const currentDate = new Date(monday);
    currentDate.setDate(monday.getDate() + i);
    
    const lessonsCount = i === 5 || i === 6 ? 3 : 2;
    
    for (let j = 0; j < lessonsCount; j++) {
      const style = styles[Math.floor(Math.random() * styles.length)];
      const teacher = teachersNames.find(t => t.style.includes(style.name)) || teachersNames[Math.floor(Math.random() * teachersNames.length)];
      
      const hour = 10 + j * 3 + Math.floor(Math.random() * 2);
      
      schedule.push({
        id: `${weekOffset}-${i}-${j}`,
        date: currentDate.toISOString().split('T')[0],
        day: days[i],
        style: style.name,
        teacher: teacher.nickname,
        time: `${hour}:00`,
        color: style.color
      });
    }
  }

  return schedule.sort((a, b) => new Date(a.date) - new Date(b.date) || a.time.localeCompare(b.time));
};
